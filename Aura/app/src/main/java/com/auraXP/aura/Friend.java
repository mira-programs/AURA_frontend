package com.auraXP.aura;

public class Friend {
    private String name;
    private String xp;

    public Friend(String name, String xp) {
        this.name = name;
        this.xp = xp;
    }

    public String getName() {
        return name;
    }

    public String getXp() {
        return xp;
    }
}
