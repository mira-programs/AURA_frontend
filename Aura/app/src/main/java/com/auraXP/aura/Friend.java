package com.auraXP.aura;

public class Friend {
    private String name;
    private int xp;

    public Friend(String name, int xp) {
        this.name = name;
        this.xp = xp;
    }

    public String getName() {
        return name;
    }

    public int getXp() {
        return xp;
    }
}
